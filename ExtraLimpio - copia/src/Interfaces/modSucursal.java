    private void txtAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtAceptarActionPerformed
       
          if(txtDireccion.getText().equals("")||txtEmail.getText().equals("")||txtId.getText().equals(""))
        {
            JOptionPane.showMessageDialog(null, "Debe llenar todos los campos");
            
        }
         else{
             try{
             
             Conexion c = new Conexion();
             Connection reg = c.getConnection();
             Comuna com = (Comuna) cbComuna.getSelectedItem();
             String idcom = Integer.toString(com.getId_comuna());
             ps = reg.prepareStatement("UPDATE sucursal SET direccion_sucursal=?, correo_sucursal=?, fk_comuna="+idcom+" where Id_sucursal=?");
             ps.setString(1, txtDireccion.getText());
             ps.setString(2, txtEmail.getText());  
             ps.setString(3, txtId.getText());
             int res = ps.executeUpdate();
             if (res>0) {
                JOptionPane.showMessageDialog(null, "Registro Actualizado con Ã©xito");
                Limpiar();
                
            }
            else
            {
                JOptionPane.showMessageDialog(null, "Error al ingresar datos");
                Limpiar();
            }
        
             } catch (Exception e){
                 JOptionPane.showMessageDialog(null, "error : "+e.toString());
             } 
         }
        
        
    }//GEN-LAST:event_txtAceptarActionPerformed

