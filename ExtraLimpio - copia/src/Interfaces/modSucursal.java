    private void txtDireccionKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtDireccionKeyPressed
        if (evt.getKeyCode() == evt.VK_ENTER){
            if(txtDireccion.getText().length()>50){
                getToolkit().beep();
                evt.consume();
                JOptionPane.showMessageDialog(this, "El largo no debe ser superior a 50","Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_txtDireccionKeyPressed

    private void txtDireccionKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtDireccionKeyTyped
        if((int)evt.getKeyChar()>32 && (int)evt.getKeyChar()<=47
            ||(int)evt.getKeyChar()>=58 && (int)evt.getKeyChar()<=64
            || (int)evt.getKeyChar()>=91 && (int)evt.getKeyChar()<=96
            || (int)evt.getKeyChar()>=123 && (int)evt.getKeyChar()<=255)
        {

            getToolkit().beep();
            evt.consume();
            JOptionPane.showMessageDialog(this, "Ingrese caracteres validos","Error", JOptionPane.ERROR_MESSAGE);

        }
    }//GEN-LAST:event_txtDireccionKeyTyped

