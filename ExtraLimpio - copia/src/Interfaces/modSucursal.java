    private void cbRegionItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cbRegionItemStateChanged

        if (evt.getStateChange() == ItemEvent.SELECTED){
            Region reg = (Region) cbRegion.getSelectedItem();
            Provincia prov = new Provincia();
            DefaultComboBoxModel modProvincia = new DefaultComboBoxModel(prov.mostrarProvincias(reg.getId_region()));
            cbProvincia.setModel(modProvincia);
            cbComuna.removeAllItems();
        }

    }//GEN-LAST:event_cbRegionItemStateChanged

