    private void btAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAceptarActionPerformed
        if (txtRut.getText().equals("")||
                cbSucursal.getSelectedItem().equals("Selecione Sucursal")||txtNombre.getText().equals("")||
                txtApellidoPaterno.getText().equals("")||txtApellidoMaterno.getText().equals("")||txtDireccion.getText().equals("")||
                txtTelefono.getText().equals("")
                ) JOptionPane.showMessageDialog(null, "Debe llenar todos los campos");

        
        else{
            try{
                int id = Integer.parseInt(idLabel.getText());
                String rut = txtRut.getText();
                String nombre = txtNombre.getText();
                String apepat = txtApellidoPaterno.getText();
                String apemat = txtApellidoMaterno.getText();
                String direccion = txtDireccion.getText();
                int telefono = Integer.parseInt(txtTelefono.getText());
                Sucursal suc = (Sucursal) cbSucursal.getSelectedItem();
                int idsuc = suc.getIdSucursal();
                
                int r = Vendedor.updateVendedor(id,rut,nombre,apepat,apemat,direccion,telefono,idsuc);
          
                if (r>0) {
                    JOptionPane.showMessageDialog(null, "Registro Actualizado con Ã©xito");
                    
                }
                else
                {
                    JOptionPane.showMessageDialog(null, "Error al Actualizar datos");
                    
                }

            } catch (Exception e){
                JOptionPane.showMessageDialog(null, "error : "+e.toString());
            }
        }

    }//GEN-LAST:event_btAceptarActionPerformed

