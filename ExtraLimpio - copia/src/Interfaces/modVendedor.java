    private void txtRutKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtRutKeyTyped
        char c = evt.getKeyChar();
        if(Character.isLetter(c) && c!='K' && c!='k')
        {
            getToolkit().beep();
            evt.consume();
            JOptionPane.showMessageDialog(this, "Ingrese solo numeros o letra K (puede ingresar guiones)","Error", JOptionPane.ERROR_MESSAGE);

        }
        else if((int)evt.getKeyChar()>=32 && (int)evt.getKeyChar()<=44
             || (int)evt.getKeyChar()>=46 && (int)evt.getKeyChar()<=47
             ||(int)evt.getKeyChar()>=58 && (int)evt.getKeyChar()<=64
             || (int)evt.getKeyChar()>=91 && (int)evt.getKeyChar()<=96
             || (int)evt.getKeyChar()>=123 && (int)evt.getKeyChar()<=255)
        {
            getToolkit().beep();
            evt.consume();
            JOptionPane.showMessageDialog(this, "Ingrese solo numeros o letra K (puede ingresar guiones)","Error", JOptionPane.ERROR_MESSAGE);

        }

    }//GEN-LAST:event_txtRutKeyTyped

