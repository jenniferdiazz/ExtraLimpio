    private void btAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAgregarActionPerformed
         if(txtDireccion.getText().equals("")||txtEmail.getText().equals("")||txtTelefono.getText().equals(""))
        {
            JOptionPane.showMessageDialog(null, "Debe llenar todos los campos");
            
        }
         else{
             try{
             PreparedStatement prs;
             Conexion c = new Conexion();
             Connection reg = c.getConnection();
             String idsuc = idLabel.getText();
             Comuna com = (Comuna) cbComuna.getSelectedItem();
             String idcom = Integer.toString(com.getId_comuna());
             ps = reg.prepareStatement("INSERT into sucursal(id_sucursal,direccion_sucursal, correo_sucursal, fk_comuna) values("+idsuc+",?,?,"+idcom+")");
             ps.setString(1, txtDireccion.getText());
             ps.setString(2, txtEmail.getText());  
             prs = reg.prepareStatement("INSERT into telefonos_sucursal (id_telefonos_sucursal, numero, fk_sucursal) values("+Sucursal.idIncrementTelSuc()+",?,"+idsuc+")");
             int res = ps.executeUpdate();
             if (res>0) {
                int a = Integer.parseInt(idLabel.getText())+1;
                idLabel.setText(Integer.toString(a));
                JOptionPane.showMessageDialog(null, "Registro Agregado con éxito");
                Limpiar();
                Actualizar();
            }
            else
            {
                JOptionPane.showMessageDialog(null, "Error al ingresar datos");
                Limpiar();
               
            }
            reg.close();
            
             } catch (Exception e){
                 JOptionPane.showMessageDialog(null, "error : "+e.toString());
             } 
         }
            
        
    }//GEN-LAST:event_btAgregarActionPerformed

    private void btEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btEliminarActionPerformed
        
        int fila = tableSucursal.getSelectedRowCount();
        if (fila < 1 ){
            JOptionPane.showMessageDialog(null, "Seleccione un registro de la tabla");
        } else{
            int respuesta = JOptionPane.showConfirmDialog(null, "¿Esta seguro?", "Alerta!", JOptionPane.YES_NO_OPTION);
            if (respuesta == 0){
            Sucursal.Eliminardato(tableSucursal.getValueAt(tableSucursal.getSelectedRow(),0).toString());
      
            }
        }
        
        
        
        
    }//GEN-LAST:event_btEliminarActionPerformed

