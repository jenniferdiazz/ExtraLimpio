    public boolean isEmail(String correo){
// Patrón para validar el email
        Pattern pattern = Pattern
                .compile("^[\\w\\\\\\+]+(\\.[\\w\\\\]+)*@([A-Za-z0-9-]+\\.)+[A-Za-z]{2,4}$");
 
        // El email a validar
        
 
        Matcher mather = pattern.matcher(correo);
 
        if (mather.find() == true) {
            return true;
        } else {
            return false;
        }
}
    public void DatosTabla(){
        PreparedStatement ps, prep;
        ResultSet rs, res;
        Connection conn = Conexion.getConnection();
        DefaultTableModel model = (DefaultTableModel) tableSucursal.getModel();
        model.addColumn("ID");
        model.addColumn("Nombre Sucursal");
        model.addColumn("Direccion");
        model.addColumn("Correo");
        model.addColumn("Comuna");


        try {
            String sql = "select id_sucursal, nombre_sucursal, direccion_sucursal,correo_sucursal, fk_comuna from sucursal";
            ps = conn.prepareStatement(sql);
            rs = ps.executeQuery();
   
                
            while (rs.next()) {

                prep = conn.prepareStatement("select * from comuna where comuna_id="+rs.getString("fk_comuna"));
                res = prep.executeQuery();
                while (res.next()) {
                               model.addRow(new Object[]{rs.getString("id_sucursal"), rs.getString("nombre_sucursal"), rs.getString("direccion_sucursal"), 
                rs.getString("correo_sucursal"), res.getString("comuna_nombre")});
                }

            }
   
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null,"Error :"+e.toString());

        }
        
    }
    public void Actualizar(){
        
        
        DefaultTableModel model = new DefaultTableModel();
        tableSucursal.setModel(model);
        try{
        PreparedStatement ps = null;
        ResultSet rs = null;
        Conexion con = new Conexion();
        Connection conn = con.getConnection();
        String sql = "SELECT * FROM sucursal";
        ps = conn.prepareStatement(sql);
        rs = ps.executeQuery();
        ResultSetMetaData rsMd = rs.getMetaData();
        int cant = rsMd.getColumnCount();
        model.addColumn("ID");
        model.addColumn("Nombre Sucursal");
        model.addColumn("Direccion");
        model.addColumn("Correo");
        model.addColumn("ID Comuna");
        while(rs.next()){
            Object[] filas = new Object[cant];
            for ( int i = 0 ; i<cant; i++){
                filas[i] = rs.getObject(i+1);          
            }
          model.addRow(filas);  
        }
        
        } catch (SQLException e){
            
        }
        
        
    }
    
